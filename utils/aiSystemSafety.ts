export interface SlowBurnResult{detected:boolean;conversationAgedays:number;benignPhaseDays:number;financialRequestDetected:boolean;riskScore:number;}
export function detectSlowBurnScam(messages:Array<{text:string;timestamp:number;fromUser:boolean}>,financialKeywords:string[]=['send money','wire transfer','crypto','bitcoin','gift card','investment','trading platform']):SlowBurnResult{
if(messages.length<5)return{detected:false,conversationAgedays:0,benignPhaseDays:0,financialRequestDetected:false,riskScore:0};
const first=Math.min(...messages.map(m=>m.timestamp)),last=Math.max(...messages.map(m=>m.timestamp));
const ageDays=(last-first)/86_400_000;
const financialMsg=messages.find(m=>financialKeywords.some(k=>m.text.toLowerCase().includes(k))&&!m.fromUser);
if(!financialMsg)return{detected:false,conversationAgedays:Math.round(ageDays),benignPhaseDays:0,financialRequestDetected:false,riskScore:0};
const benignDays=(financialMsg.timestamp-first)/86_400_000;
const riskScore=Math.min(Math.round((benignDays>14?40:benignDays>7?25:10)+(ageDays>30?30:ageDays>14?20:10)),100);
if(riskScore>=25)writeAuditLog('ai.slow_burn_scam',{ageDays,benignDays,riskScore}).catch(()=>{});
return{detected:riskScore>=25,conversationAgedays:Math.round(ageDays),benignPhaseDays:Math.round(benignDays),financialRequestDetected:true,riskScore};}
export const slowBurnDetect=detectSlowBurnScam;

