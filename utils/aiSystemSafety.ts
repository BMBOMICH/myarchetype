export interface AiAnomalyResult{anomaly:boolean;score:number;indicators:string[];action:'allow'|'flag'|'block';}
export function detectAiTrafficAnomaly(requests:Array<{endpoint:string;responseTimeMs:number;statusCode:number;timestamp:number}>):AiAnomalyResult{
if(requests.length<10)return{anomaly:false,score:0,indicators:[],action:'allow'};
const i:string[]=[];
const avgRt=requests.reduce((s,r)=>s+r.responseTimeMs,0)/requests.length;
const errorRate=requests.filter(r=>r.statusCode>=500).length/requests.length;
const recentSpike=requests.filter(r=>Date.now()-r.timestamp<60_000).length;
if(avgRt>5000)i.push(`high_latency:${Math.round(avgRt)}ms`);
if(errorRate>0.1)i.push(`high_error_rate:${Math.round(errorRate*100)}%`);
if(recentSpike>100)i.push(`request_spike:${recentSpike}/min`);
const score=Math.min(i.length*33,100);
const action=score>=66?'block':score>=33?'flag':'allow';
if(action!=='allow')writeAuditLog('ai.traffic_anomaly',{indicators:i,score}).catch(()=>{});
return{anomaly:i.length>0,score,indicators:i,action};}
